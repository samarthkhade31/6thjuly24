package Variable1;

public class Demo2
{
	public static void main(String[] args)
	{
		//Variable Declaration
		
		String studentName;
		int studentROLLno;
		char studentgrade;										//Data Type Variable Name 
		float studentperc;
		String studentdept;
		char studentsection;
		
		//Assign Value
		
		studentName=("Samarth");
		studentROLLno=249;	 									//Variable Name Variable Value 
		studentgrade='A';
		studentperc=91.25f;
		studentdept="CSE";
		studentsection='B';
		
		//Use Variable
		
		System.out.println(studentName);
		System.out.println(studentROLLno);						//Info Reuse
		System.out.println(studentgrade);
		System.out.println(studentperc);
		System.out.println(studentdept);
		System.out.println(studentsection);
	}
	
	
}
