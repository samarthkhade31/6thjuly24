package Variable1;

public class Demo5 
{  
	public static void main(String[] args) 
	{
		
		// Data type 							Size							Range
		byte num1; 							//1byte							-128 to 127
		num1= 127;
		
		short num2;                         //2bytes                        -32,768 to 32,7673                    
		num2= 27673;
				
		int num3;                           //4bytes                        -2,147.483,648 to 2,147,483,647
		num3= 2147483647;
		
		long num4;                          //8bytes             -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
		num4= 253356454;   
				
		String S1;                          //No Size
		S1= "bsfwukfhabjfeufkhjnasfnjdgxnvnmnbrjhgdskj";
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(S1);
	}
	
	
	

}
